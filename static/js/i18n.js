  // Language is URL-scoped (?lang=zh|en) so two windows can stay independent.
  // localStorage is only the default when the URL has no lang param.
  function readLangFromUrl() {
    try {
      const v = new URLSearchParams(location.search).get('lang');
      if (v === 'zh' || v === 'en') return v;
    } catch (_) {}
    return null;
  }

  function readStoredLang() {
    try {
      const v = localStorage.getItem('perv.lang');
      if (v === 'zh' || v === 'en') return v;
    } catch (_) {}
    return null;
  }

  function syncLangToUrl(lang) {
    try {
      const url = new URL(location.href);
      if (url.searchParams.get('lang') === lang) return;
      url.searchParams.set('lang', lang);
      history.replaceState(null, '', url.pathname + url.search + url.hash);
    } catch (_) {}
  }

  /** Append / update ?lang= on same-origin navigational URLs. */
  function withLang(href, lang) {
    if (href == null || href === '') return href;
    const raw = String(href);
    if (
      raw.charAt(0) === '#' ||
      /^mailto:/i.test(raw) ||
      /^tel:/i.test(raw) ||
      /^javascript:/i.test(raw) ||
      /^data:/i.test(raw)
    ) {
      return raw;
    }
    try {
      const url = new URL(raw, location.href);
      if (url.origin !== location.origin) return raw;
      url.searchParams.set('lang', lang);
      return url.pathname + url.search + url.hash;
    } catch (_) {
      return raw;
    }
  }

  function decorateInternalLinks(root) {
    const scope = root && root.querySelectorAll ? root : document;
    scope.querySelectorAll('a[href]').forEach((a) => {
      if (a.hasAttribute('download')) return;
      const href = a.getAttribute('href');
      if (!href) return;
      const next = withLang(href, I18n.lang);
      if (next !== href) a.setAttribute('href', next);
    });
  }

  const I18n = {
    lang: readLangFromUrl() || readStoredLang() || 'en',
    t(key) {
      return (dict[this.lang] && dict[this.lang][key]) || dict.zh[key] || key;
    },
    withLang(href) {
      return withLang(href, this.lang);
    },
    setLang(l) {
      if (!dict[l]) return;
      this.lang = l;
      try { localStorage.setItem('perv.lang', l); } catch (_) {}
      syncLangToUrl(l);
      this.apply();
      document.dispatchEvent(new CustomEvent('i18nchange', { detail: { lang: l } }));
    },
    apply() {
      document.documentElement.lang = this.lang === 'zh' ? 'zh-CN' : 'en';
      document.querySelectorAll('[data-i18n]').forEach((el) => {
        el.textContent = this.t(el.dataset.i18n);
      });
      document.querySelectorAll('[data-i18n-ph]').forEach((el) => {
        el.setAttribute('placeholder', this.t(el.dataset.i18nPh));
      });
      // data-i18n-tip: resolved at show-time by tooltip.js via I18n.t()
      // Legacy: data-i18n-title → data-i18n-tip
      document.querySelectorAll('[data-i18n-title]').forEach((el) => {
        if (!el.dataset.i18nTip) el.dataset.i18nTip = el.dataset.i18nTitle;
        el.removeAttribute('data-i18n-title');
        el.removeAttribute('title');
      });
      const titleKey = document.body && document.body.dataset.titleKey;
      if (titleKey) document.title = this.t(titleKey);
      document.querySelectorAll('.lang-switch button').forEach((b) => {
        b.classList.toggle('active', b.dataset.lang === this.lang);
      });
      decorateInternalLinks(document);
      document.documentElement.classList.remove('i18n-loading');
      if (window.PervTip) {
        window.PervTip.migrate(document);
        window.PervTip.refresh();
      }
    },
  };

  window.I18n = I18n;

  // Rewrite href before left / middle / modified clicks so deep links keep lang.
  document.addEventListener('mousedown', (ev) => {
    const a = ev.target && ev.target.closest && ev.target.closest('a[href]');
    if (!a || a.hasAttribute('download')) return;
    const href = a.getAttribute('href');
    if (!href) return;
    const next = withLang(href, I18n.lang);
    if (next !== href) a.setAttribute('href', next);
  }, true);

  document.addEventListener('DOMContentLoaded', () => {
    document.querySelectorAll('.lang-switch button').forEach((b) => {
      b.addEventListener('click', () => I18n.setLang(b.dataset.lang));
    });
    // Pin current lang into the URL so refresh / share / in-page nav stay scoped.
    syncLangToUrl(I18n.lang);
    I18n.apply();
  });
